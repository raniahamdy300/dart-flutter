// Switch Case with Seasons
// Objective: Use switch for multiple cases.
// Instructions:
// - Create an integer variable month with a value between 1 and 12.
// - Use a switch statement to print the season based on the month.

void main() {
  int month = 5;
  switch (month) {
    case 1:
      print('Jan');
      break;
    case 2:
      print('Feb');
      break;
    case 3:
      print('Mar');
      break;
    case 4:
      print('Apr');
      break;
    case 5:
      print('May');
      break;
    case 6:
      print('June');
      break;
    case 7:
      print('July');
      break;
    case 8:
      print('Aug');
      break;
    case 9:
      print('Sep');
      break;
    case 10:
      print('Oct');
      break;
    case 11:
      print('Nov');
      break;
    case 12:
      print('Dec');
      break;
  }
}
